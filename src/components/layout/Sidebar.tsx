const getNavItems = (role: UserRole): NavItem[] => {
  switch (role) {
    case "STUDENT":
      return studentNavItems;
    case "SUPERVISOR":
      return supervisorNavItems;
    case "ADMIN":
      return adminNavItems;
    default:
      return [];
  }
};

const getRoleColor = (role: UserRole): string => {
  switch (role) {
    case "STUDENT":
      return "indigo";
    case "SUPERVISOR":
      return "purple";
    case "ADMIN":
      return "blue";
    default:
      return "gray";
  }
};

